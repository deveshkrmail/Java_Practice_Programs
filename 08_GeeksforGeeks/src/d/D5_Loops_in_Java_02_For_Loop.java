package d;

public class D5_Loops_in_Java_02_For_Loop 
{

	public static void main(String[] args) 
	{
		int i;
		String str01[]= {"Name01","Name02","Name03","Name04"};
		for(i=0;i<str01.length;i++)
		{
			System.out.println(str01[i]);
		}
		
		int int01[]= {2,3,4,5,9,8,6,6,9,9};
		for(i=0;i<int01.length;i++)
		{
			System.out.println(int01[i]);
		}
	}

}
