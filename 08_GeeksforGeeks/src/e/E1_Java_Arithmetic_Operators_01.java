package e;

public class E1_Java_Arithmetic_Operators_01 
{

	public static void main(String[] args) 
	{
		int num01=3,num02=9;
		int sum, sub, mul, mod;
		double div;
		sum=num01+num02;
		sub=num01-num02;
		mul=num01*num02;
		div=num01/num02;
		mod=num01%num02;
		
		System.out.println("num01+num02="+sum);
		System.out.println("num01-num02="+sub);
		System.out.println("num01*num02="+mul);
		System.out.println("num01/num02="+div);
		System.out.println("num01%num02="+mod);
	}

}
